    private void button_GOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_GOActionPerformed
        // TODO add your handling code here:
        if( choice_timezone.getSelectedItem().equals("EST") ) { START_TIME = "13:11"; END_TIME = "13:12"; }
        else if (choice_timezone.getSelectedItem().equals("CST") ) { START_TIME = "09:59"; END_TIME = "10:02"; }
        else { START_TIME = "07:59"; END_TIME = "8:02"; }
        if(!activeRetrying) {
            try {
                label_status.setText("Checking for item...");
                if(bot.itemOnPage())
                    bot.start();
                else
                {
                    if(checkbox_autoretry.getState()) // if autoretry is on
                    {
                        label_status.setText("Waiting until " + START_TIME);
                        runTimechecker();
                        button_GO.setText("Stop Bot");
                        activeRetrying=true;
                    }
                    else // if no autoretry
                    {
                        JOptionPane.showMessageDialog(null, "ERROR: Item not found.");
                        label_status.setText("");
                    }
                }
            } catch ( java.lang.NullPointerException e ) { JOptionPane.showMessageDialog(null, "Settings not loaded into bot. Use settings tab."); System.exit(0); }
        }
        else
        {
            tcheck.stop();
            try{ checker.stop(); } catch (java.lang.NullPointerException e ) { }
            activeRetrying=false;
            button_GO.setText("Start Bot");
            label_status.setText("");
            retrycount=0;
        }
    }//GEN-LAST:event_button_GOActionPerformed

