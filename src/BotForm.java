    public void runClock()
    {
        Timer t = new Timer (50, updateClockAction);
        t.start();
    }
    ActionListener updateClockAction = new ActionListener()
    {
        public void actionPerformed(ActionEvent e)
        {
            format = new SimpleDateFormat("HH:mm:ss");
            d = new Date();
            timeString = format.format(d);
            label_time.setText(timeString);
        }
    };

    Timer checker;
    public void runAutochecker()
    {
        checker = new Timer(250, checkForItem);
        checker.start();
    }
    ActionListener checkForItem = new ActionListener()
    {
        public void actionPerformed(ActionEvent e)
        {
            boolean keepchecking=false;
            if(format.format(d).substring(0,5).equals(START_TIME) && !keepchecking) keepchecking=true;
            else if(format.format(d).substring(0,5).equals(END_TIME)) keepchecking=false;
            if(keepchecking)
            {
                retrycount++;
                label_status.setText("Auto-refreshing...retry number " + retrycount);
                if(bot.itemOnPage())
                {
                    bot.start();
                    checker.stop();
                   // bot.start();
                }
            }
        }
    };
    Timer tcheck;
    public void runTimechecker()
    {
        tcheck = new Timer(2500, checkTime);
        tcheck.start();

    }
    ActionListener checkTime = new ActionListener()
    {
        public void actionPerformed(ActionEvent e)
        {
            if(format.format(d).substring(0,5).equals(START_TIME))
            {
                runAutochecker();
                tcheck.stop();
            }
        }
    };
